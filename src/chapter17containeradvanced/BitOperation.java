public class BitOperation {
    public static void main(String[] args) {
        System.out.println("(1<<2) = " + (1 << 2));
        System.out.println("(1<<10) = " + (1 << 10));
        System.out.println("(1024>>9) = " + (1024 >> 9));
        System.out.println("(1000>>9) = " + (1000 >> 9));
//        (1<<2) = 4
//        (1<<10) = 1024
//        (1024>>9) = 2
//        (1000>>9) = 1
    }
}
